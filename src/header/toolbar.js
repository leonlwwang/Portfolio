const toolbarOpenIcon = `
  <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor"
      stroke-width="2" stroke-linecap="round" stroke-linejoin="round" class="feather feather-x-square">
      <rect x="3" y="3" width="18" height="18" rx="2" ry="2"></rect>
      <line x1="9" y1="9" x2="15" y2="15"></line>
      <line x1="15" y1="9" x2="9" y2="15"></line>
  </svg>
`

const toolbarClosedIcon = `
  <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor"
    stroke-width="2" stroke-linecap="round" stroke-linejoin="round" class="feather feather-pen-tool">
    <path d="M12 19l7-7 3 3-7 7-3-3z"></path>
    <path d="M18 13l-1.5-7.5L2 2l3.5 14.5L13 18l5-5z"></path>
    <path d="M2 2l7.586 7.586"></path>
    <circle cx="11" cy="11" r="2"></circle>
  </svg>
`

let opened = false

export const enableToolbar = (toolbar) => {
  const tools = toolbar.querySelector('ul')
  const toggle = toolbar.querySelector('svg')

  toggle.addEventListener('pointerdown', () => {
    opened = !opened
    if (opened) {
      toggle.innerHTML = toolbarOpenIcon
      tools.className = 'open'
    } else {
      toggle.innerHTML = toolbarClosedIcon
      tools.className = 'closed'
    }
  })
}
